import groupBy from 'lodash/groupBy';

const jemoticons = getJemoticons();
const groupedByCategory = groupBy(jemoticons, 'cat');
const categories = Object.keys(groupedByCategory);
const jemoticonHashmap = categories.reduce((aggregator, category) => {
  const categoryJemoticons = groupedByCategory[category];
  const categoryHashMap = categoryJemoticons.reduce((agg, jemoticon, index) => ({
    // nested reducer:（/｡＼)
    ...agg,
    [`${category}-${index}`]: jemoticon.emo,
  }), {});

  return {
    ...aggregator,
    ...categoryHashMap,
  };
}, {});

export default jemoticonHashmap;

